package com.netxforge.oss2.jobs.internal;

import org.osgi.framework.BundleContext;

import com.google.inject.AbstractModule;
import com.google.inject.Module;
import com.netxforge.base.di.OSS2Activator;

public class JobActivator extends OSS2Activator {

	private static BundleContext context;

	static BundleContext getContext() {
		return context;
	}

	/*
	 * (non-Javadoc)
	 * @see org.osgi.framework.BundleActivator#start(org.osgi.framework.BundleContext)
	 */
	public void start(BundleContext bundleContext) throws Exception {
		JobActivator.context = bundleContext;
	}

	/*
	 * (non-Javadoc)
	 * @see org.osgi.framework.BundleActivator#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext bundleContext) throws Exception {
		JobActivator.context = null;
	}

	@Override
	protected Module getModule() {
		
		return new AbstractModule(){

			@Override
			protected void configure() {
				// Will be needed for various scheduler implementations...
				// for now we only support Quartz. 
			}
			
		};
	}

}
